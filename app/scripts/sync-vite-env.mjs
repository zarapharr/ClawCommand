#!/usr/bin/env node
import fs from 'node:fs';
import path from 'node:path';

const appDir = process.cwd();
const envLocalPath = path.join(appDir, '.env.local');
const rootWorkspaceEnv = path.resolve(appDir, '../../.env');

function parseEnvFile(filePath) {
  if (!fs.existsSync(filePath)) return {};
  const content = fs.readFileSync(filePath, 'utf8');
  const out = {};
  for (const line of content.split(/\r?\n/)) {
    const trimmed = line.trim();
    if (!trimmed || trimmed.startsWith('#')) continue;
    const idx = trimmed.indexOf('=');
    if (idx <= 0) continue;
    const key = trimmed.slice(0, idx).trim();
    const value = trimmed.slice(idx + 1).trim();
    out[key] = value.replace(/^"|"$/g, '');
  }
  return out;
}

const existingLocal = parseEnvFile(envLocalPath);
const rootEnv = parseEnvFile(rootWorkspaceEnv);

const base =
  existingLocal.VITE_OPENCLAW_API_BASE ||
  rootEnv.VITE_OPENCLAW_API_BASE ||
  rootEnv.OPENCLAW_API_BASE ||
  rootEnv.OPENCLAW_GATEWAY_URL ||
  'http://127.0.0.1:3001';

const token =
  existingLocal.VITE_OPENCLAW_API_TOKEN ||
  rootEnv.VITE_OPENCLAW_API_TOKEN ||
  rootEnv.OPENCLAW_API_TOKEN ||
  rootEnv.OPENCLAW_GATEWAY_TOKEN ||
  '';

const actionEndpoint =
  existingLocal.VITE_RUNTIME_ACTION_ENDPOINT ||
  rootEnv.VITE_RUNTIME_ACTION_ENDPOINT ||
  `${base.replace(/\/$/, '')}/api/runtime/actions`;

const ledgerEndpoint =
  existingLocal.VITE_RUNTIME_LEDGER_ENDPOINT ||
  rootEnv.VITE_RUNTIME_LEDGER_ENDPOINT ||
  `${base.replace(/\/$/, '')}/api/runtime/ledger`;

const output = [
  '# Auto-generated by scripts/sync-vite-env.mjs',
  '# Safe to keep. Regenerated before dev/build/test.',
  `VITE_OPENCLAW_API_BASE=${base}`,
  `VITE_OPENCLAW_API_TOKEN=${token}`,
  `VITE_RUNTIME_ACTION_ENDPOINT=${actionEndpoint}`,
  `VITE_RUNTIME_LEDGER_ENDPOINT=${ledgerEndpoint}`,
  '',
].join('\n');

fs.writeFileSync(envLocalPath, output, 'utf8');
console.log(`✅ Synced ${envLocalPath}`);
